#include<stdio.h>
int main() {
    int a, b, c,sum;
    a = 1;
    b = 1;
    sum = 0;
    while(b < 4000000) {//4000000万以内
        if(b % 2 == 0) sum += b;
        c = b;
        b += a;
        a = c;
    }   
    printf("%d\n", sum);
    return 0;
}
